#count()
a = "Hello World"
print(a.count("o"))

#encode()
a = "Hello World"
print(a.encode())

#endswith()
a = "Hello World"
print(a.endswith("d"))

#expandtabs()
a = "Hello\tWorld"
print(a.expandtabs(4))

#find()
a = "Hello World"
